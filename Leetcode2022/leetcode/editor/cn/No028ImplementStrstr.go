func strStr(haystack string, needle string) int {
	idx := -1
	for i := 0; i < len(haystack); i++ {
		if haystack[i] != needle[0] {
			continue
		}
		hasMatch := true
		for j := 1; j < len(needle); j++ {
			if i+j >= len(haystack) {
				hasMatch = false
				break
			}
			if haystack[i+j] != needle[j] {
				hasMatch = false
				continue
			}
		}
		if hasMatch {
			idx = i
			break
		}
	}
	return idx
}

