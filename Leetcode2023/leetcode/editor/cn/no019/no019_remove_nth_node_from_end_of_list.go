/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func removeNthFromEnd(head *ListNode, n int) *ListNode {
	if head == nil {
		return head
	}
	dummy := head
	l := 0
	for dummy != nil {
		l++
		dummy = dummy.Next
	}
	if l < n {
		return head
	}

	loop := head
	c := l - n
	for c > 1 {
		c--
		loop = loop.Next
	}
	loop.Next = loop.Next.Next
	return head
}

