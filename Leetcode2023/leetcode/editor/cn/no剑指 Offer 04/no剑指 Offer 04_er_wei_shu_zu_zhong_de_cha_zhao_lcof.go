func findNumberIn2DArray(matrix [][]int, target int) bool {
	if len(matrix) == 0 || len(matrix[0]) == 0 {
		return false
	}

	// // 普通做法
	//for i := 0; i < len(matrix); i++ {
	//	for j := 0; j < len(matrix[0]); j++ {
	//		if matrix[i][j] == target {
	//			return true
	//		}
	//	}
	//}

	//
	m, n := len(matrix), len(matrix[0])
	i, j := 0, n-1
	for i < m && j >= 0 {
		if matrix[i][j] == target {
			return true
		}
		if matrix[i][j] < target {
			i++
		} else {
			j--
		}
	}

	return false
}

